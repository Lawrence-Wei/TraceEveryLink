export function useI18n() {
  const [language, setLanguageState] = useState<Language>(defaultLanguage);

  useEffect(() => {
    const saved = window.localStorage.getItem(languageStorageKey);
    if (isLanguage(saved)) {
      setLanguageState(saved);
    }
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
    window.localStorage.setItem(languageStorageKey, language);
  }, [language]);

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
  }, []);

  const t = useCallback<Translate>((key, params) => {
    const template = translations[language][key] || translations.en[key] || key;
    if (!params) return template;
    return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? `{${name}}`));
  }, [language]);

  return { language, setLanguage, t };
}

export function translateLoginError(t: Translate, rawError: string | null | undefined) {
  if (!rawError) return "";
  return t(loginErrorMap[rawError] || "login.error.generic");
}

export function translateApiError(t: Translate, rawError: string | null | undefined, fallback: TranslationKey) {
  if (!rawError) return t(fallback);
  return t(apiErrorMap[rawError] || fallback);
}

export function isLanguage(value: string | null): value is Language {
  return value === "zh-CN" || value === "en";
}
